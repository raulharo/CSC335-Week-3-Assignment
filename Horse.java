public class Horse
{
   // Define the Horse class here
   String name;
   String color;
   int birthYear;

   public String getName(){
       return name;
   }

   public String getColor(){
       return color;
   }

   public int getBirthYear(){
       return birthYear;
   }

   public void setName(String param){
       name = param;
   }

   public void setColor(String param){
       color = param;
   }

   public void setBirthYear(int param){
       birthYear = param;
   }
}
